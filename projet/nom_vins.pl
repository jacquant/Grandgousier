 replaces_vin([P|List], X, In, Out) :-
   replace_vin(P, X, In, Out);
   replaces_vin(List, X, In, Out).

 replace_vin(L,X,In,Out) :-
    append(L,Suf,In), !, Out = [X|Suf].
 replace_vin(_,_,[],[]) :- !.
 replace_vin(L,X,[H|In],[H|Out]) :-
    replace_vin(L,X,In,Out).
